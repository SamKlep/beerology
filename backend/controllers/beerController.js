import asyncHandler from 'express-async-handler'
import Beer from '../models/beerModel.js'

// @desc    Fetch all beers
// @route   GET /api/beers
// @access  Public
const getBeers = asyncHandler(async (req, res) => {
  const beers = await Beer.find({})

  res.json({ beers })
})

// @desc    Fetch single beer
// @route   GET /api/beers/:id
// @access  Public
const getBeerById = asyncHandler(async (req, res) => {
  const beer = await Beer.findById(req.params.id)

  if (beer) {
    res.json(beer)
  } else {
    res.status(404)
    throw new Error('Beer not found')
  }
})

// @desc    Delete a beer
// @route   DELETE /api/beers/:id
// @access  Private/Admin
const deleteBeer = asyncHandler(async (req, res) => {
  const beer = await Beer.findById(req.params.id)

  if (beer) {
    await beer.remove()
    res.json({ message: 'Beer removed' })
  } else {
    res.status(404)
    throw new Error('Beer not found')
  }
})

